"""main.py's flow, re-expressed on the MAF graph workflow API (WorkflowBuilder).

============================================================================
DISCLAIMER: THIS FILE WAS GENERATED BY CLAUDE (at the user's explicit request).
It is a learning companion to main.py — the imperative original is left
untouched. Read it critically; don't treat it as your own hand-written work.
Run it the same way as main.py (from inside project/):  uv run workflow_graph.py
============================================================================

Design rationale (why this shape, not a "pure" cycle of agent-nodes):
  Every AgentExecutor owns its OWN session (agent_framework/_workflows/
  _agent_executor.py:177). That makes a multi-turn *conversation* awkward to model
  as a cycle of agent-nodes: two nodes can't share one growing history without
  threading `full_conversation` down every edge and re-feeding it each turn. So
  the interactive front half lives inside ONE custom executor (GatherRequirements)
  that owns the dev session and loops via request_info; the deterministic back
  half (write -> code -> review -> fix) is where real agent-nodes + conditional
  edges + the iteration cap actually pay off. This mirrors the conclusion reached
  for the imperative version: graphs are worst at dynamic, human-driven control
  flow, best at deterministic orchestration.

Two wins this gives us over main.py for free:
  1. No session pollution. main.py shares ONE session between Soham and the Judge,
     so the Judge's verdicts leak into Soham's history. Here the Judge runs
     STATELESS on its own transcript (see GatherRequirements), and every
     AgentExecutor node has its own isolated session.
  2. Bounded loops. WorkflowBuilder(max_iterations=...) caps total supersteps
     (DEFAULT_MAX_ITERATIONS = 100), so the XL<->Amma review loop can't ping-pong
     forever the way main.py's `while True` can. (Caveat: it bounds the WHOLE run,
     front half included — see build_workflow().)
"""

