@executor(id="write_proposal")
async def write_proposal(
    details: ProjectDetails, ctx: WorkflowContext[AgentExecutorRequest]
) -> None:
    # We write proposal.md ourselves (deterministic) instead of letting an agent
    # do it — same reasoning as main.py. write_to_file is a @tool but stays
    # directly callable.
    project_path = OUTPUTS_DIR / details.project_name
    proposal_file_path = write_to_file(
        filename="proposal.md", contents=details.proposal, filepath=project_path
    )
    # Stash the dynamic paths in the SHARED workflow state (one State per run) so
    # the review / fix / finalize steps downstream can read them.
    ctx.set_state("project_path", str(project_path))
    ctx.set_state("proposal_file_path", str(proposal_file_path))

    await ctx.send_message(
        AgentExecutorRequest(
            messages=[
                Message(
                    role="system",
                    contents=[
                        f"Read the contents of this proposal: {proposal_file_path} "
                        f"and code a working solution at {project_path / 'out'} "
                        "directory. IMPORTANT: All project related files (source "
                        "code, readme etc.) should be mandatorily inside the 'out' "
                        "directory. Once you are done coding, inform the user and "
                        "give him the path where your code is."
                    ],
                )
            ],
            should_respond=True,
        )
    )


@executor(id="to_review")
async def to_review(
    _coded: AgentExecutorResponse, ctx: WorkflowContext[AgentExecutorRequest]
) -> None:
    # XL's reply is irrelevant to Amma — Amma reviews the directory on disk. So we
    # discard the coding response and emit a fresh review request.
    out_dir = Path(ctx.get_state("project_path")) / "out"
    await ctx.send_message(
        AgentExecutorRequest(
            messages=[
                Message(
                    role="system",
                    contents=[f"Review the code under directory: {out_dir}"],
                )
            ],
            should_respond=True,
        )
    )


@executor(id="to_fix")
async def to_fix(
    review: AgentExecutorResponse, ctx: WorkflowContext[AgentExecutorRequest]
) -> None:
    comments = review.agent_response.value.comments
    await ctx.send_message(
        AgentExecutorRequest(
            messages=[
                Message(
                    role="system",
                    contents=[
                        "Code review has flagged the following issues: "
                        + comments
                        + " Fix them. If needed, make sure you read existing code "
                        + "to verify correctness before writing your fixes."
                    ],
                )
            ],
            should_respond=True,
        )
    )


def review_passed(review: AgentExecutorResponse) -> bool:
    """Edge condition: stop the XL<->Amma loop when the review is done.

    NOTE — conditions receive exactly what the upstream node sent; here that is
    Amma's AgentExecutorResponse, and `.agent_response.value` is the already-parsed
    CodeReviewOutput (Amma has response_format baked in). Unlike main.py — which
    raised on a missing value and would otherwise spin forever — we treat "no
    value" as a stop, so a flaky review ends the loop instead of looping. The
    framework deliberately does NOT swallow exceptions in conditions (_edge.py), so
    keep this total and side-effect free.
    """
    verdict = review.agent_response.value
    return verdict is None or verdict.lgtm or not verdict.success


class PresentProposal(Executor):
    """Soham announces the proposal and promises the product BEFORE coding starts.

    Claude: positioned between write_proposal and xl so the "you'll have a finished
    product shortly" message lands in the same place main.py emits it (before XL
    codes), NOT after the review. It also forwards XL's coding request downstream.
    Reuses the dev session so the summary has full conversational context.
    """

    def __init__(self, dev_agent, dev_session, id: str = "present"):
        super().__init__(id=id)
        self._dev = dev_agent
        self._session = dev_session

    @handler
    async def run(
        self,
        xl_request: AgentExecutorRequest,
        ctx: WorkflowContext[AgentExecutorRequest, str],
    ) -> None:
        proposal_file_path = ctx.get_state("proposal_file_path")
        summary = await self._dev.run(
            Message(
                role="system",
                contents=[
                    "Summarize the discussion, tell the user that the proposal "
                    f"file is present at {proposal_file_path}, give him the name, "
                    "and tell him he will have a finished product shortly. DO NOT "
                    "ask any questions at this stage, as this is supposed to be the "
                    "final approach."
                ],
            ),
            session=self._session,
        )
        await ctx.yield_output(summary.text)  # emitted with executor_id="present"
        await ctx.send_message(xl_request)  # -> xl starts coding


@executor(id="done")
async def done(_review: AgentExecutorResponse) -> None:
    # Claude: terminal sink. Matching main.py, there is no extra Soham message after
    # the review — XL already reported where the code is and Amma's review was shown.
    # Receiving Amma's passing response with nothing further sent lets the run go
    # idle, which is how the driver detects completion.
    pass


def build_workflow():
    # ONE dev session, shared by the gatherer and the presenter so Soham keeps full
    # conversational memory end to end. XL and Amma get their own isolated sessions
    # (created internally by AgentExecutor).
    dev_session = sm_agent.create_session()
    gather = GatherRequirements(sm_agent, judge_agent, dev_session)
    present = PresentProposal(sm_agent, dev_session)
    xl = AgentExecutor(xl_agent, id="xl")
    amma = AgentExecutor(amma_agent, id="amma")

    # NOTE — max_iterations is the safety cap main.py's `while True` lacks, but it
    # bounds the WHOLE run (every superstep, conversation included), not just the
    # review loop. Default is 100; a very long Q&A could approach it.
    return (
        WorkflowBuilder(start_executor=gather, max_iterations=100)
        .add_edge(gather, write_proposal)
        # Soham announces the proposal (product-shortly message) BEFORE XL codes.
        .add_edge(write_proposal, present)
        .add_edge(present, xl)
        .add_edge(xl, to_review)
        .add_edge(to_review, amma)
        # Switch-case = exactly-one routing (vs. add_edge conditions, where every
        # matching edge fires). Done -> done sink; otherwise -> fix, then loop to XL.
        .add_switch_case_edge_group(
            amma,
            [Case(condition=review_passed, target=done), Default(target=to_fix)],
        )
        .add_edge(to_fix, xl)
        .build()
    )


# Claude: map executor ids -> the agent persona behind them, so outputs are
# attributed correctly. Every AgentExecutor yields its OWN response as a workflow
# output (_agent_executor.py:432), so xl and amma each surface here; without this
# map the old driver mislabeled Amma's review as "Soham".
EXECUTOR_LABELS = {"present": "Soham", "xl": "XL", "amma": "Amma"}


def _print_segment_outputs(result) -> None:
    # A WorkflowRunResult is a list of the events from THAT run() call, so printing
    # its outputs each segment surfaces messages in the order they're produced
    # (Soham's announce, then XL's code report, then Amma's review) rather than as
    # one batch at the end.
    for event in result:
        if event.type == "output":
            text = getattr(event.data, "text", None) or str(event.data)
            label = EXECUTOR_LABELS.get(event.executor_id, event.executor_id)
            print(f"\n[AGENT {label}]: {text}")


async def take_input_from_user() -> str:
    return await asyncio.to_thread(input, "\n[USER]: ")


async def answer_pending_requests(requests) -> dict:
    """Turn the workflow's pending request_info events into a responses dict.

    NOTE — two kinds of request flow through here:
      1. UserPrompt  — our own conversational asks from GatherRequirements.
      2. function-approval requests — auto-surfaced by AgentExecutor when XL calls
         write_to_file (approval_mode="always_require"). The AgentExecutor converts
         the agent's user_input_requests into workflow request_info events
         (_agent_executor.py:434-439) and resumes the agent once we answer. So the
         `always_require` approval survives the port for free — we just reuse the
         same `to_function_approval_response` call main.py used.
    """
    responses = {}
    for event in requests:
        data = event.data
        if isinstance(data, UserPrompt):
            if data.text:
                print(f"\n[AGENT Soham]: {data.text}")
            responses[event.request_id] = await take_input_from_user()
        elif hasattr(data, "to_function_approval_response"):
            print(f"\n{event.source_executor_id} needs approval:")
            print(f" Function: {data.function_call.name}")
            print(f" Arguments: {data.function_call.arguments}")
            print("Enter 'y' or 'n'.")
            approved = (await take_input_from_user()).strip().lower() == "y"
            responses[event.request_id] = data.to_function_approval_response(
                approved=approved
            )
        else:
            raise RuntimeError(f"Unexpected request payload: {type(data)!r}")
    return responses


async def main():
    workflow = build_workflow()

    # NOTE — this driver loop is the imperative shell the workflow can't absorb:
    # run -> collect pending human/approval requests -> answer -> resume. The
    # `responses=` kwarg is how you feed answers back in (_workflow.py:519). The run
    # is done when a resume returns with no more pending requests.
    result = await workflow.run("start")
    while True:
        # Surface this segment's agent outputs (labeled by source) before pausing
        # for the next human turn, so the transcript reads in chronological order.
        _print_segment_outputs(result)
        requests = result.get_request_info_events()
        if not requests:
            break
        responses = await answer_pending_requests(requests)
        result = await workflow.run(responses=responses)


if __name__ == "__main__":
    asyncio.run(main())
